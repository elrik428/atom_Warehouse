--•	Approved transactions
select month(dtstamp),sum(TAMOUNT) , count(*)
from dbo.TRANSLOG_TRANSACT_2017 a
where
	 DESTCOMID = 'NET_NTBN'
	 and MSGID in('0200','0220')
   and PROCCODE not in ('020000','200000')
	 and TRESPONSE = '00'
	 and substring(mask,1,6) not in ('418680','422152','422164','442317','445822','459346','470659','482428','491791','503348','533824','535142','535143','535551','540574','547261','549804','552053','553262','554272','558786','582479','589242','676937','677311','403348','405050','410788','410789','413420','516297','521098','527800','527801','527802','527803','527804','527805','527806','527807','527808','527809','527890','527891','527892','527893')
 group by month(dtstamp)
 order by month(dtstamp)

