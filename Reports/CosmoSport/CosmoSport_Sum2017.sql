WHEN '37' THEN 'AMEX'
WHEN '30' THEN 'Diners'
WHEN '36' THEN 'Diners'
WHEN '38' THEN 'Diners'
ELSE 'UNKNOWN TYPE'
END as card_type ,sum(AMOUNT), count(*)
from abc096.IMP_TRANSACT_D_2017 a
left join abc096.Products_old b on b.BIN = substring(a.MASK,1,6)
left join abc096.Banks c on b.BANKID = c.ID
where MID = '000000120005000'
group by c.BANK, SUBSTRING(mask,1,2)
order by c.BANK, SUBSTRING(mask,1,2)
